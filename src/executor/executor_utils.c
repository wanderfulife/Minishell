#include "builtins.h"
#include "executor.h"
#include <sys/wait.h>
#include <unistd.h>

int	executor_count_commands(t_command *cmd)
{
	int	count;

	count = 0;
	while (cmd)
	{
		count++;
		cmd = cmd->pipe_next;
	}
	return (count);
}

void	executor_reset_fds(int saved_stdin, int saved_stdout)
{
	dup2(saved_stdin, STDIN_FILENO);
	dup2(saved_stdout, STDOUT_FILENO);
	close(saved_stdin);
	close(saved_stdout);
}
